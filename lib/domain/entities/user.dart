class User {
  final String name;
  final String lastName;
  final String email;
  final String role;
  final String gender;
  final String phone;
  final String code;

  User({
    required this.name,
    required this.lastName,
    required this.email,
    required this.role,
    required this.gender,
    required this.phone,
    required this.code	
  });
}
